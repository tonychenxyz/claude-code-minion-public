#!/usr/bin/env node

import { SlackBot } from './slack-bot.js';
import { SessionManager } from './session-manager.js';
import { TerminalManager } from './terminal-manager.js';
import { Session, OAuthToken } from './types.js';
import * as fs from 'fs';
import * as os from 'os';
import * as path from 'path';
import { fileURLToPath } from 'url';

// Handle the known socket-mode bug where "server explicit disconnect"
// arrives during "connecting" state - just log and let it reconnect
process.on('uncaughtException', (error) => {
  if (error.message?.includes("Unhandled event 'server explicit disconnect'")) {
    console.log('[Socket] Disconnect during reconnect - ignoring, will retry...');
    return;
  }
  console.error('Uncaught exception:', error);
  process.exit(1);
});

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// App directory is where this script lives (app/dist -> app/)
const appDirectory = path.resolve(__dirname, '..');
// Root directory is parent of app/
const rootDirectory = path.resolve(appDirectory, '..');

interface Config {
  slackBotToken: string;
  slackAppToken: string;
  orchestratorPort: number;
  workingDirectory: string;
  appDirectory: string;
  oauthTokens: OAuthToken[];
}

function loadConfig(): Config {
  // Try to load from .env file in root directory
  const envPath = path.join(rootDirectory, '.env');
  if (fs.existsSync(envPath)) {
    const envContent = fs.readFileSync(envPath, 'utf-8');
    for (const line of envContent.split('\n')) {
      const [key, ...valueParts] = line.split('=');
      if (key && valueParts.length > 0) {
        const value = valueParts.join('=').trim().replace(/^["']|["']$/g, '');
        process.env[key.trim()] = value;
      }
    }
  }

  const slackBotToken = process.env.SLACK_BOT_TOKEN;
  const slackAppToken = process.env.SLACK_APP_TOKEN;
  const orchestratorPort = parseInt(process.env.ORCHESTRATOR_PORT || '3000', 10);
  // Default working directory is root (parent of app/)
  const defaultWorkingDir = rootDirectory;
  const workingDirectory = process.env.WORKING_DIRECTORY || defaultWorkingDir;

  if (!slackBotToken) {
    console.error('Error: SLACK_BOT_TOKEN is required');
    console.error('Set it in .env file or as environment variable');
    process.exit(1);
  }

  if (!slackAppToken) {
    console.error('Error: SLACK_APP_TOKEN is required');
    console.error('Set it in .env file or as environment variable');
    process.exit(1);
  }

  // Parse the Claude Code OAuth token, when provided.
  const oauthTokens: OAuthToken[] = [];

  if (process.env.CLAUDE_CODE_OAUTH_TOKEN) {
    oauthTokens.push({
      alias: 'default',
      token: process.env.CLAUDE_CODE_OAUTH_TOKEN,
      isDefault: true,
    });
  }

  // Also parse OpenAI tokens for Codex support
  if (process.env.OPENAI_API_KEY) {
    // Make it available but don't add to Claude OAuth tokens array
    // Codex strategy reads OPENAI_API_KEY directly from env
    console.log('OpenAI API key configured');
  }

  // If no tokens found, warn but don't exit (some users might use ANTHROPIC_API_KEY)
  if (oauthTokens.length === 0) {
    console.warn('Warning: No CLAUDE_CODE_OAUTH_TOKEN found');
    console.warn('Set CLAUDE_CODE_OAUTH_TOKEN in .env for Claude Code, or use Codex/ANTHROPIC_API_KEY');
  }

  // Ensure exactly one default token
  const hasDefault = oauthTokens.some(t => t.isDefault);
  if (!hasDefault && oauthTokens.length > 0) {
    oauthTokens[0].isDefault = true;
  }

  return {
    slackBotToken,
    slackAppToken,
    orchestratorPort,
    workingDirectory,
    appDirectory,
    oauthTokens,
  };
}

// Idempotent setup of <repo>/.codex/ — the single shared CODEX_HOME for all channels.
// Writes config.toml (model, sandbox, MCP server entry — no per-channel env, those flow
// down via PTY env inheritance) and symlinks AGENTS.md + skills/ from the repo.
function setupSharedCodexHome(workingDirectory: string, appDirectory: string, orchestratorPort: number): void {
  const codexHome = path.join(workingDirectory, '.codex');
  try {
    fs.mkdirSync(codexHome, { recursive: true });

    const model = process.env.CODEX_MODEL || 'gpt-5.5';
    const reasoningEffort = process.env.CODEX_REASONING_EFFORT || 'xhigh';
    const mcpServerJsPath = path.join(appDirectory, 'dist', 'mcp-server.js');

    const configToml =
      `# Auto-generated by claude-code-minion at startup.\n` +
      `# Static defaults; per-channel CHANNEL_ID/MCP_PORT/ORCHESTRATOR_URL flow in via PTY env inheritance.\n` +
      `model = "${model}"\n` +
      `model_reasoning_effort = "${reasoningEffort}"\n` +
      `approval_policy = "never"\n` +
      `sandbox_mode = "workspace-write"\n` +
      `\n` +
      `[sandbox_workspace_write]\n` +
      `network_access = true\n` +
      `\n` +
      `[mcp_servers.slack-messenger]\n` +
      `command = "node"\n` +
      `args = [${JSON.stringify(mcpServerJsPath)}]\n`;

    const configPath = path.join(codexHome, 'config.toml');
    fs.writeFileSync(configPath, configToml);

    // Symlink AGENTS.md -> ../CLAUDE.md (so codex sees the same instructions)
    const codexAgentsPath = path.join(codexHome, 'AGENTS.md');
    if (!fs.existsSync(codexAgentsPath) && !fs.lstatSync(codexAgentsPath, { throwIfNoEntry: false })) {
      try {
        fs.symlinkSync('../CLAUDE.md', codexAgentsPath);
      } catch (e) {
        console.warn('Could not symlink .codex/AGENTS.md:', e);
      }
    }

    // Symlink skills/ -> ../.claude/skills/
    const codexSkillsPath = path.join(codexHome, 'skills');
    const repoSkillsPath = path.join(workingDirectory, '.claude', 'skills');
    if (fs.existsSync(repoSkillsPath) && !fs.lstatSync(codexSkillsPath, { throwIfNoEntry: false })) {
      try {
        fs.symlinkSync('../.claude/skills', codexSkillsPath);
      } catch (e) {
        console.warn('Could not symlink .codex/skills:', e);
      }
    }

    // Symlink auth.json -> ~/.codex/auth.json so `codex login` (run in either location)
    // keeps both in sync, and token refreshes propagate automatically.
    const codexAuthPath = path.join(codexHome, 'auth.json');
    const homeAuthPath = path.join(os.homedir(), '.codex', 'auth.json');
    if (fs.existsSync(homeAuthPath) && !fs.lstatSync(codexAuthPath, { throwIfNoEntry: false })) {
      try {
        fs.symlinkSync(homeAuthPath, codexAuthPath);
      } catch (e) {
        console.warn('Could not symlink .codex/auth.json:', e);
      }
    }

    console.log(`Shared CODEX_HOME ready at ${codexHome} (model=${model}, effort=${reasoningEffort})`);
    void orchestratorPort; // reserved for future use (env-based config)
  } catch (error) {
    console.warn('Could not set up shared .codex/:', error);
  }
}

async function main() {
  console.log('═══════════════════════════════════════════════════════════');
  console.log('           Claude Code Minion - Slack Bot');
  console.log('═══════════════════════════════════════════════════════════');

  const config = loadConfig();

  console.log(`App directory: ${config.appDirectory}`);
  console.log(`Working directory: ${config.workingDirectory}`);
  console.log(`Orchestrator port: ${config.orchestratorPort}`);

  // Log configured Claude Code token without exposing secret material.
  if (config.oauthTokens.length > 0) {
    console.log('Claude Code OAuth token configured');
    for (const token of config.oauthTokens) {
      const defaultLabel = token.isDefault ? ' (default)' : '';
      console.log(`  - ${token.alias}${defaultLabel}`);
    }
  }

  // Ensure working directory exists
  if (!fs.existsSync(config.workingDirectory)) {
    fs.mkdirSync(config.workingDirectory, { recursive: true });
    console.log(`Created working directory: ${config.workingDirectory}`);
  }

  // Create AGENTS.md symlink -> CLAUDE.md if it doesn't exist (for Codex compatibility)
  const agentsMdPath = path.join(config.workingDirectory, 'AGENTS.md');
  const claudeMdPath = path.join(config.workingDirectory, 'CLAUDE.md');
  if (fs.existsSync(claudeMdPath) && !fs.existsSync(agentsMdPath)) {
    try {
      fs.symlinkSync('CLAUDE.md', agentsMdPath);
      console.log('Created AGENTS.md -> CLAUDE.md symlink');
    } catch (error) {
      console.warn('Could not create AGENTS.md symlink:', error);
    }
  }

  // Set up shared <repo>/.codex/ — one CODEX_HOME for every channel
  setupSharedCodexHome(config.workingDirectory, config.appDirectory, config.orchestratorPort);

  // Initialize managers
  const sessionManager = new SessionManager(config.workingDirectory, config.oauthTokens);

  // Initialize Slack bot (need to create early to pass callback to terminalManager)
  const bot = new SlackBot(
    config.slackBotToken,
    config.slackAppToken,
    config.workingDirectory,
    config.appDirectory,
    sessionManager,
    null as any, // Will set terminalManager after creating it
    config.oauthTokens
  );

  // Create terminal manager with callbacks to bot
  const terminalManager = new TerminalManager(
    config.workingDirectory,
    config.appDirectory,
    (channelId, message) => bot.handleQueueProcess(channelId, message),
    (channelId) => bot.handleAgentTurnComplete(channelId)
  );

  // Wire up the terminal manager to the bot
  (bot as any).terminalManager = terminalManager;

  // Check for existing session with a known user (for restart notifications)
  const existingSessions: Session[] = Array.from((sessionManager as any).sessions?.values() || []);
  const existingUserSession = existingSessions.find((s) => s.userId && s.dmChannelId);

  // Create a session token on startup
  const session = sessionManager.createSession(
    existingUserSession?.userId || '', // Reuse existing user ID if available
    existingUserSession?.dmChannelId || '', // Reuse existing DM channel if available
    config.workingDirectory
  );

  console.log('');
  console.log('═══════════════════════════════════════════════════════════');
  console.log('  SESSION TOKEN: ' + session.token);
  console.log('═══════════════════════════════════════════════════════════');
  console.log('');
  console.log('To connect:');
  console.log('1. DM the bot in Slack with this token');
  console.log('2. Create a channel and invite the bot');
  console.log('3. Start chatting with Claude Code!');
  console.log('');

  // Start orchestrator server (receives messages from MCP servers)
  process.env.ORCHESTRATOR_PORT = config.orchestratorPort.toString();
  bot.startOrchestratorServer(config.orchestratorPort);

  // Start the bot
  await bot.start();

  // Recreate live PTYs for channel sessions persisted before a restart.
  // Agent session IDs are stored separately and are picked up on the first turn.
  await bot.restorePersistedChannelSessions();

  // If we have an existing user, DM them the new session token
  if (existingUserSession?.userId && existingUserSession?.dmChannelId) {
    try {
      await bot.sendDirectMessage(
        existingUserSession.dmChannelId,
        `🔄 *Bot restarted!*\n\nNew session token: \`${session.token}\`\n\nYour existing channel sessions should continue working.`
      );
      console.log(`Sent restart notification to user ${existingUserSession.userId}`);
    } catch (error) {
      console.error('Failed to send restart notification:', error);
    }
  }

  // Handle shutdown
  process.on('SIGINT', async () => {
    console.log('\nShutting down...');
    await bot.stop();
    process.exit(0);
  });

  process.on('SIGTERM', async () => {
    console.log('\nShutting down...');
    await bot.stop();
    process.exit(0);
  });
}

main().catch((error) => {
  console.error('Failed to start:', error);
  process.exit(1);
});
